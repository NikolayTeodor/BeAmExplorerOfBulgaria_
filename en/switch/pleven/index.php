<h3>Panorama “Pleven’s epopee 1877”</h3>
Panorama “Pleven’s epopee 1877” is an art museum in Pleven, built in honor of the 100th anniversary of the liberation of Pleven from Ottoman rule. In the first three years after its opening panorama was visited by 2.5 million people. It is among nearly 200 monuments muilt by Pleven’s citizens in memory of the fallen Russians, Romanians and Bulgarians. It is included in the Hundred national tourist sites.
