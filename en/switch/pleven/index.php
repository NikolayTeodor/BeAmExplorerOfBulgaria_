<h3>Regional Historical Museum</h3>
Regional Historical Museum dates back to the foundation in 1903 Archeological Society in the city, which creates its own collection of objects, the first of them discovered during excavations in the fortress Storgozia. In 1923 the collection is displayed in the building of “Consent” and in 1953 the museum became an independent institution. In 1984 it was moved to its current building.</td>
