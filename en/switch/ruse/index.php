<h3>Ivanovo Rock Churches</h3>
Ivanovo Rock Churches are located 18 km. south of Ruse. They are distinguished from other preserved rock monasteries in Bulgaria with well-preserved frescoes. Unlike traditional monasteries, which consist of 1-2 churches, monastic and economic part in Ivanovo has branched network of small rock churches, chapels and cells hewn at various heights in the rocks of the picturesque canyon of the river Rousse Lom and related trails and rock stairs.
