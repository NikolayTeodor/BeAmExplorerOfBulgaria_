<h3>Belogradchik Rocks</h3>
Belogradchik Rocks are rock figures high up to 200 m, located in West Fore, near the town of Belogradchik. The rocks vary in color from pure red to yellow. Many of the rocks have fantastic shapes and are involved in interesting legends. They are often referred to people or objects which resemble or remind. Rocks are listed as natural landmarks of the Bulgarian government and the main tourist attraction in the region.
