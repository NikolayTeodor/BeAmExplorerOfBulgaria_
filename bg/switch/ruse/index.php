<h3>Ивановски скални църкви</h3>
Ивановските скални църкви се намират на 18 км. южно от град Русе. Отличават се от другите запазени скални манастирски комплекси в България с добре запазените си стенописи. За разлика от традиционните манастири, които се състоят от 1–2 църкви, монашеска и стопанска част, в Иваново има разклонена мрежа от малки скални църкви, параклиси и килии, издълбани на различна височина в скалите на живописния каньон на река Русенски Лом и свързани с пътеки и скални стълби.
