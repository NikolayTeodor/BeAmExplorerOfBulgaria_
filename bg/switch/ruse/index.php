<h3>Паметник на Свободата</h3>
Паметникът на Свободата в Русе е проектиран в началото на XX век от италианския скулптор Арнолдо Цоки, а е изработен от Георги Киселинчев. С течение на времето е придобил значение като един от символите на града и е част от герба му.
